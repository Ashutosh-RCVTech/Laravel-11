    <h1>Single Action Controller</h1>
    <p>In Laravel, an invokable controller is a controller with only one method called __invoke(). It's used when a controller is responsible for handling a single action. Instead of defining multiple methods inside the controller, you define just the __invoke method, and Laravel will automatically call it when the route points to that controller.
It keeps the code clean and simple for single-action routes.</p>
